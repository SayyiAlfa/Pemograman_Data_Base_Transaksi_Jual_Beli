    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        TableModel model = table1.getModel();
        int index [] = table1.getSelectedRows();
        
        Object [] row = new Object[4];
        
        Report rpt = new Report();
        DefaultTableModel model1 = (DefaultTableModel)rpt.jTable.getModel();
        
        for(int i = 0; i < index.length; i++ )
        {
            row[0] = model.getValueAt(index[i], 0);
            row[1] = model.getValueAt(index[i], 1);
            row[2] = model.getValueAt(index[i], 2);
            row[3] = model.getValueAt(index[i], 3);
            
            model1.addRow(row);
        }            
         rpt.setVisible(true);
         rpt.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
    }//GEN-LAST:event_jButton1ActionPerformed

