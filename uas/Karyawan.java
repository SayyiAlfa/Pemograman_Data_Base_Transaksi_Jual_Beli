    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int baris = table.rowAtPoint(evt.getPoint());
        String nik = table.getValueAt(baris,1).toString();
        jNIK.setText(nik);
        
        String nama = table.getValueAt(baris,2).toString();
        jNama.setText(nama);
        
        String alamat = table.getValueAt(baris,3).toString();
        jAlamat.setText(alamat);
        
        String telpon = table.getValueAt(baris,4).toString();
        jTelpon.setText(telpon);
    }//GEN-LAST:event_tableMouseClicked

