    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "INSERT INTO karyawan VALUES('"+jNIK.getText()+"','"+jNama.getText()+"','"+jAlamat.getText()+"','"+jTelpon.getText()+"')";
            java.sql.Connection conn = Koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            this.TampilData();
            this.ClearData();
        }   
       catch(SQLException e){
           JOptionPane.showMessageDialog(this, e.getMessage());
       }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "UPDATE karyawan SET nama=?, alamat=?, telpon=? WHERE nik=?";
            java.sql.Connection conn = (Connection)Koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.setString(1, jNama.getText());
            pstm.setString(2, jAlamat.getText());
            pstm.setString(3, jTelpon.getText());
            pstm.setString(4, jNIK.getText());
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            this.TampilData();
            this.ClearData();
        } catch(SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
}

    }//GEN-LAST:event_btnUbahActionPerformed

